#!/usr/bin/env python3
# -*- coding: utf-8 -*-
class Calculation(object):
    @staticmethod
    def add(a, b):
        return a+b
    @staticmethod
    def minus(a, b):
        return a-b
    @staticmethod
    def multiple(a, b):
        return a*b
    @staticmethod
    def divide(a, b):
        return a/b
